/** Returnează troparul sfântului zilei (sau null dacă nu există tropar verificat). */
export function getTroparSfantForDate(date: Date): TroparSfant | null {
  const mm = String(date.getMonth() + 1).padStart(2, "0");
  const dd = String(date.getDate()).padStart(2, "0");
  const key = `${mm}-${dd}`;
  return tropareSfinti.find((t) => t.dateKey === key) ?? null;
}
