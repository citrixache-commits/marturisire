/**
 * Returnează apoftegma pentru ziua dată — același text pentru toți utilizatorii,
 * rotește zilnic în baza day-of-year. Fără random — deterministic.
 */
export function getApoftegmaForDate(date: Date): Apoftegma {
  const start = new Date(Date.UTC(date.getUTCFullYear(), 0, 0));
  const diff = date.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  const idx = ((dayOfYear % pateric.length) + pateric.length) % pateric.length;
  return pateric[idx];
}
